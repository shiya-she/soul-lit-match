CREATE TABLE IF NOT EXISTS writers (
  id SERIAL PRIMARY KEY,
  type_code VARCHAR(4) NOT NULL UNIQUE,
  name VARCHAR(100) NOT NULL,
  title VARCHAR(200) NOT NULL,
  era VARCHAR(50) NOT NULL,
  works VARCHAR(500) NOT NULL,
  keywords VARCHAR(500) NOT NULL,
  portrait VARCHAR(10) NOT NULL,
  quote VARCHAR(500) NOT NULL,
  match_reason TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS
ALTER TABLE writers ENABLE ROW LEVEL SECURITY;

-- Allow read access to anyone (the matching logic is server-side, writers are public)
CREATE POLICY "writers_are_readable" ON writers
  FOR SELECT
  USING (true);

